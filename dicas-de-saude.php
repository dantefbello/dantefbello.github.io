                                    <h3><a name="o-tratamento-ortodontico-cirurgico"></a>O Tratamento ortodôntico cirúrgico</h3>
									<!--a title="Image Title" href="images/1.jpg" rel="prettyPhoto[gallery1]"><img src="http://ortodontiadantebello.thiagottomasi.com/images/1.jpg"  alt="" class="aligncenter" width="650" height="140"	/></a><br /-->			
              É um tipo de abordagem terapêutica que  envolve um tratamento ortodôntico (aparelho fixo), associado a um procedimento  cirúrgico. Esta forma de tratamento é realizada em pacientes que não estão mais  em crescimento e apresentam uma discrepância esquelética entre o complexo  nasomaxilar e a mandíbula.<br />
                Este tratamento é realizado em duas fases:<br /><br />

                <ol start="1" type="1" style="padding:0 0 0px 0">
                  <li>Ortodôntica,       onde é feito o alinhamento, nivelamento e as descompensações necessárias a       cada caso.</li>
                  <li>Cirúrgica,       que vai corrigir o problema esquelético. Após, o tratamento é finalizado       pelo ortodontista.</li>
                </ol>
<p>&nbsp;</p>
                                    
                                    
                                    <h3><a name="por-que-das-extracoes-dentarias"></a>Por que das extrações dentárias?</h3>
									<!--a title="Image Title" href="images/1.jpg" rel="prettyPhoto[gallery1]"><img src="http://ortodontiadantebello.thiagottomasi.com/images/1.jpg"  alt="" class="aligncenter" width="650" height="140"	/></a><br /-->										
			  <p>Em alguns pacientes existe uma grande diferença  entre o tamanho dos dentes e&nbsp; a base óssea, caracterizada por uma falta de  espaço, impedindo que os dentes se posicionem de forma correta. Nestes casos,  faz-se necessário a extração de um ou mais dentes. Também, existem outras  situações clínicas onde é necessário a extração dentária.</p>
                                    
                                    <h3><a name="por-que-corrigir-a-pocicao-dental"></a>Por que corrigir a posição dental?</h3>
									<!--a title="Image Title" href="images/1.jpg" rel="prettyPhoto[gallery1]"><img src="http://ortodontiadantebello.thiagottomasi.com/images/1.jpg"  alt="" class="aligncenter" width="650" height="140"	/></a><br /-->										
			  <p>Nos dias atuais a estética tem sido muito  valorizada, seja nas relações humanas ou na melhora da auto-estima das pessoas.  Em algumas situações clínicas, torna-se importante e muitas vezes necessária a  correção por motivos funcionais, ou seja, a busca do equilíbrio entre dentes,  músculos e articulação têmporo-mandibular.</p>
                                    
                                     <h3><a name="os-dentes-se-movimentam-apos-o-tratamento"></a>Os dentes se movimentam após o tratamento?</h3>
									<!--a title="Image Title" href="images/1.jpg" rel="prettyPhoto[gallery1]"><img src="http://ortodontiadantebello.thiagottomasi.com/images/1.jpg"  alt="" class="aligncenter" width="650" height="140"	/></a><br /-->										
			  <p>Após a correção dos dentes, com os aparelhos, o  paciente é instruído a usar o que é denominado de contenção, por um determinado  tempo. Com o envelhecimento (processo fisiológico), os dentes tendem a  modificar sua posição, que é variável de pessoa a pessoa.</p>
                                    
                                     <h3><a name="tempo-de-tratamento"></a>Tempo de tratamento?</h3>
									<!--a title="Image Title" href="images/1.jpg" rel="prettyPhoto[gallery1]"><img src="http://ortodontiadantebello.thiagottomasi.com/images/1.jpg"  alt="" class="aligncenter" width="650" height="140"	/></a><br /-->										
			  <p>O tempo de tratamento ortodôntico está  diretamente relacionado ao problema que o paciente apresenta, idade, resposta  biológica, colaboração e do tipo de aparatologia utilizada para corrigir  determinada má-oclusão. A grande maioria dos tratamentos é corrigida em um prazo que varia de&nbsp; 18  a 36 meses.</p>
                                    
                                     <h3><a name="qual-a-melhor-tecnica-de-tratamento"></a>Qual a melhor técnica de tratamento?</h3>
